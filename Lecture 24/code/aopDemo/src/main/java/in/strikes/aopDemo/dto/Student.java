package in.strikes.aopDemo.dto;

import jdk.jfr.Timestamp;
import org.springframework.stereotype.Component;

@Timestamp
public class Student {
    private String name;
    private Integer age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
